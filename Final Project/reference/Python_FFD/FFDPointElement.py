

class FFDPointElement(object):

    def __init__(self, xInitial, yInitial, zInitial):
        self.x = xInitial
        self.y = yInitial
        self.z = zInitial

    def getX(self):
        return self.x

    def getY(self):
        return self.y

    def getZ(self):
        return self.z

    def setX(self, x):
        self.x = x

    def setY(self, y):
        self.y = y

    def setZ(self, z):
        self.z = z



